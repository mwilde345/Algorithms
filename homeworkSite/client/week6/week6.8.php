  <h3 class="panel-heading">8. Give a list of the number of permutations of n things for n=1 to n=100 (you will need BigIntegers).  Is it feasible to run through all the permutations of 100 things to solve a problem? Give reasons.</h3>
  <div class="panel-body">
	<h4>Permutations of N represented by n!<br>When n=100, the number of permutations is a 158 digit number.<br>
	Thus there are at least 10^158 operations that must be done to check all permutations.<br>
	Going through all these permutations would take years, thus it's not feasible.</h4>
    <pre><code>
		package Permutation;

		import java.math.BigInteger;
		import java.util.ArrayList;
		import java.util.HashMap;
		import java.util.Map;

		public class ListPermutationSize {
			public static void main(String[] args){
				ListPermutationSize perm = new ListPermutationSize();
				Map&ltString, String> answer = perm.nPermsSize(100);
			}
			public Map<String,String> nPermsSize(int n){
				Map<String,String> results = new HashMap<String, String>();
				for(int i = 1; i<=n; i++){
					BigInteger fact = BigInteger.ONE;
					for(int j = 1; j<=i; j++){
						fact = fact.multiply(BigInteger.valueOf(j));
					}
					results.put("N = "+i, fact.toString()+"\n");
					System.out.println("N = "+i+", Size of number: "+fact.toString().length()+": "+ fact.toString());
				}
				return results;
			}
			
		}
